package Day19_WhileLoop_DoWhileLoop;

public class C03_DoWhileLoop {
    public static void main(String[] args) {
        int i=0;

        while (i<5){
            System.out.print(i + " ");
            i++;
        }
        int a=0;
        do {
            System.out.print(a + " ");
            a++;
        }while (a<5);
    }
}
